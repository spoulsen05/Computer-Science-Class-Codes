    cout << "Of the terrible dilemma in her misery." << endl;
    
    cout << endl;
    
    // Second portion of poem
    cout << "He doesn't know at all the poet's agony" << endl;
    cout << "Until he found a note in her balcony" << endl;
    cout << "Where a broken masterpiece was shattered in fragment" << endl;
    cout << "He redeemed it little by little just like a precious garment." << endl;
    
    cout << endl;
    
    // Third portion of poem
    cout << "Though he felt pity, he still outweighs his rants" << endl;
    cout << "For a long time sentiments he has kept for month" << endl;
